package com.test.golabang.item;

public class ItemCommentDTO {
	private String itemCommentSeq;
	private String generalSeq;
	private String itemSeq;
	private String content;
	private String starScore;
	private String regdate;
	
	public String getItemCommentSeq() {
		return itemCommentSeq;
	}
	public void setItemCommentSeq(String itemCommentSeq) {
		this.itemCommentSeq = itemCommentSeq;
	}
	public String getGeneralSeq() {
		return generalSeq;
	}
	public void setGeneralSeq(String generalSeq) {
		this.generalSeq = generalSeq;
	}
	public String getItemSeq() {
		return itemSeq;
	}
	public void setItemSeq(String itemSeq) {
		this.itemSeq = itemSeq;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getStarScore() {
		return starScore;
	}
	public void setStarScore(String starScore) {
		this.starScore = starScore;
	}
	public String getRegdate() {
		return regdate;
	}
	public void setRegdate(String regdate) {
		this.regdate = regdate;
	}
	
	
}
